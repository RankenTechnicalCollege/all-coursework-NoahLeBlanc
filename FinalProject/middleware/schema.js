import Joi from "joi";
//|====================================================================================================|
//|-------------------------------------------[-USER-SCHEMA-]------------------------------------------|
//|====================================================================================================|
//|==================================================|
//|---------------[-USER-POST-SCHEMA-]---------------|
//|==================================================|
export const userSchema = Joi.object({
  email: Joi.string().email().required(),
  password: Joi.string().trim().min(3).required(),
  givenName: Joi.string().trim().min(1).required(),
  familyName: Joi.string().trim().min(1).required(),
  role: Joi.string()
    .trim()
    .lowercase()
    .valid(
      'developer',
      'quality analyst',
      'business analyst',
      'product manager',
      'technical manager'
    )
    .required()
});

//|==================================================|
//|---------------[-USER-PATCH-SCHEMA-]--------------|
//|==================================================|
export const userPatchSchema = Joi.object({
  password: Joi.string().min(3).optional(),
  fullName: Joi.string().min(1).optional(), // This field is not in POST schema
  givenName: Joi.string().min(1).optional(),
  familyName: Joi.string().min(1).optional(),
  role: Joi.string()
    .valid(
      'developer',
      'quality analyst',
      'business analyst',
      'product manager',
      'technical manager'
    )
    .optional()
}).min(1);

//|==================================================|
//|---------------[-USER-LOGIN-SCHEMA-]--------------|
//|==================================================|
export const userLoginSchema = Joi.object({
  email: Joi.string().email().min(1).required(),
  password: Joi.string().min(3).required()
});
